#ifndef HAND_ENUM_H
#define HAND_ENUM_H

/**
 * @enum Hand
 * @brief Represents the hand (left or right) for the glove model.
 * 
 * This enum is used to specify whether the glove is intended for the left hand or the right hand.
 */
enum Hand {
    Left, ///< Represents the left hand.
    Right ///< Represents the right hand.
};

#endif
